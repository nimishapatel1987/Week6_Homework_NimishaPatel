package homework_week_6;

//program to swap two variable


public class Class15 {
    public static void main(String[] args) {
        int x, y, temp;
        x = 20;
        y = 30;
        System.out.println("Before swapping : a, b = "+x+", "+ y );
        temp = x;
        x = y;
        y = temp;
        System.out.println("After swapping : a, b = "+x+", "+ y);



    }

}
